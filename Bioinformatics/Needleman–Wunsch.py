def alignment_dp_init(x, y, d):
    dp = np.empty((len(x) + 1, len(y) + 1), dtype=object)
    dp.fill((0, "", ""))

    for i in range(len(x) + 1):
        dp[i][0] = (i * d, x[:i], "-" * i)
    for j in range(len(y) + 1):
        dp[0][j] = (j * d, "-" * j, y[:j])

    return dp

def dp_update(dp, x, y, i, j, d):
    w = [dp[i - 1][j - 1][0] + blosum62[amino_asids[x[i - 1]]][amino_asids[y[j - 1]]], 
                 dp[i - 1][j][0] + d, 
                 dp[i][j - 1][0] + d]
    
