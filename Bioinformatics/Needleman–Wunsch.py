    if w_arg_max == 0:
        dp[i][j] = (w[w_arg_max], 
                        dp[i - 1][j - 1][1] + x[i - 1],
                        dp[i - 1][j - 1][2] + y[j - 1])
    elif w_arg_max == 1:
        dp[i][j] = (w[w_arg_max], 
                        dp[i - 1][j][1] + x[i - 1],
                        dp[i - 1][j][2] + "-")
    elif w_arg_max == 2:
        dp[i][j] = (w[w_arg_max], 
                        dp[i][j - 1][1] + "-",
                        dp[i][j - 1][2] + y[j - 1])

